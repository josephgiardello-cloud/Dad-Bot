# Module-level default signer (process-scoped; one per process lifetime)
# ---------------------------------------------------------------------------

#: Default signer. Uses DADBOT_RECEIPT_KEY env var if set, else random key.
DEFAULT_SIGNER: ReceiptSigner = ReceiptSigner()
